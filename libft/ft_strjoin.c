#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*s3;
	size_t	total_len;

	total_len = ft_strlen(s1) + ft_strlen(s2) + 1;
	s3 = malloc(total_len);
	if (s3 == NULL)
		return (NULL);
	ft_strlcpy(s3, s1, ft_strlen(s1) + 1);
	ft_strlcat(s3, s2, total_len);
	return (s3);
}
