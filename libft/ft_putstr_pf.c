#include "ft_printf.h"

int	ft_putstr_pf(char *s)
{
	int	i;

	if (!s)
		return (ft_putstr_pf("(null)"));
	i = 0;
	while (*s)
	{
		write (1, s, 1);
		s++;
		i++;
	}
	return (i);
}
